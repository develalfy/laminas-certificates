<?php


namespace Certificate\Model;


class TermSheetDocument implements DocumentInterface
{
    /**
     * @var string
     */
    private $url;

    /**
     * TermSheetDocument constructor.
     * @param string $url
     */
    public function __construct(string $url)
    {
        $this->url = $url;
    }

    /**
     * @return string
     */
    public function getUrl(): string
    {
        return $this->url;
    }

    /**
     * @param string $url
     */
    public function setUrl(string $url): void
    {
        $this->url = $url;
    }
}